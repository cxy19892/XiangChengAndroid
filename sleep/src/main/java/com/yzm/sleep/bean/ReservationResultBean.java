package com.yzm.sleep.bean;

public class ReservationResultBean {
	private String kfuid;//  客服uid    
	private String name;// ：名字
	private String tx;//    头像
	private String tx_key;//    头像key
	public String getKfuid() {
		return kfuid;
	}
	public void setKfuid(String kfuid) {
		this.kfuid = kfuid;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getTx() {
		return tx;
	}
	public void setTx(String tx) {
		this.tx = tx;
	}
	public String getTx_key() {
		return tx_key;
	}
	public void setTx_key(String tx_key) {
		this.tx_key = tx_key;
	}
	
	
}
